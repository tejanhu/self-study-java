public class Microsoft implements Company{
    public void employ(){
        System.out.println("We are pleased to announce that we are hiring applicants at Microsoft ...");
    }
    public void reject(){
        System.out.println("We regret to inform you that we are unable to hire you at Microsoft. Thank you please apply again!");
    }
    public void fire(){
        System.out.println("You're FIRED!!!!");
    }
}